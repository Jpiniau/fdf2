#include "fdf.h"

t_line	ft_switch(t_line l)
{
	l.x1 = l.x1 + l.x2;
	l.x2 = l.x1 - l.x2;
	l.x1 = l.x1 - l.x2;
	l.y1 = l.y1 + l.y2;
	l.y2 = l.y1 - l.y2;
	l.y1 = l.y1 - l.y2;
	l.z1 = l.z1 + l.z2;
	l.z2 = l.z1 - l.z2;
	l.z1 = l.z1 - l.z1;
	return (l);
}
